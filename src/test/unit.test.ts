import { describe, it, expect, vi, beforeEach, afterEach } from "vitest";
import { validateIdentifier, IDENTIFIER_RE } from "../tools.js";

// ---------------------------------------------------------------------------
// validateIdentifier
// ---------------------------------------------------------------------------
describe("validateIdentifier", () => {
  it("accepts simple alphabetic names", () => {
    expect(() => validateIdentifier("Person", "label")).not.toThrow();
    expect(() => validateIdentifier("OWNS", "relationship type")).not.toThrow();
  });

  it("accepts names with underscores and digits", () => {
    expect(() => validateIdentifier("my_Label_2", "label")).not.toThrow();
    expect(() => validateIdentifier("_private", "label")).not.toThrow();
  });

  it("rejects names that start with a digit", () => {
    expect(() => validateIdentifier("1Person", "label")).toThrow(
      /Invalid label/
    );
  });

  it("rejects names containing spaces", () => {
    expect(() => validateIdentifier("my label", "label")).toThrow(
      /Invalid label/
    );
  });

  it("rejects names containing hyphens", () => {
    expect(() => validateIdentifier("my-label", "label")).toThrow(
      /Invalid label/
    );
  });

  it("rejects names with special characters", () => {
    expect(() => validateIdentifier("Label; DROP", "label")).toThrow(
      /Invalid label/
    );
  });

  it("rejects empty string", () => {
    expect(() => validateIdentifier("", "label")).toThrow(/Invalid label/);
  });
});

// ---------------------------------------------------------------------------
// IDENTIFIER_RE
// ---------------------------------------------------------------------------
describe("IDENTIFIER_RE", () => {
  it.each(["Person", "OWNS", "_private", "my_Var2", "A"])(
    "matches valid identifier: %s",
    (id) => {
      expect(IDENTIFIER_RE.test(id)).toBe(true);
    }
  );

  it.each(["1bad", "bad-name", "has space", "", "semi;colon", "dot.name"])(
    "rejects invalid identifier: %s",
    (id) => {
      expect(IDENTIFIER_RE.test(id)).toBe(false);
    }
  );
});

// ---------------------------------------------------------------------------
// runCypher tool — guarded by ENABLE_RUN_CYPHER env var
// ---------------------------------------------------------------------------
function runCypherGuard(): void {
  if (process.env.ENABLE_RUN_CYPHER !== "true") {
    throw new Error(
      "runCypher is disabled. Set ENABLE_RUN_CYPHER=true to enable it."
    );
  }
}

describe("runCypher env-var guard", () => {
  const originalEnv = process.env.ENABLE_RUN_CYPHER;

  afterEach(() => {
    if (originalEnv === undefined) {
      delete process.env.ENABLE_RUN_CYPHER;
    } else {
      process.env.ENABLE_RUN_CYPHER = originalEnv;
    }
  });

  it("throws when ENABLE_RUN_CYPHER is not set", () => {
    delete process.env.ENABLE_RUN_CYPHER;
    expect(runCypherGuard).toThrow(/runCypher is disabled/);
  });

  it("throws when ENABLE_RUN_CYPHER=false", () => {
    process.env.ENABLE_RUN_CYPHER = "false";
    expect(runCypherGuard).toThrow(/runCypher is disabled/);
  });

  it("does not throw when ENABLE_RUN_CYPHER=true", () => {
    process.env.ENABLE_RUN_CYPHER = "true";
    expect(runCypherGuard).not.toThrow();
  });
});

