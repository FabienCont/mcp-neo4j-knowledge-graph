// Cypher templates generated by tool handlers
// ---------------------------------------------------------------------------
describe("upsertEntity Cypher template", () => {
  it("uses MERGE with id parameter", () => {
    const label = "Person";
    const cypher = `MERGE (n:${label} { id: $id }) SET n += $properties RETURN n`;
    expect(cypher).toBe(
      "MERGE (n:Person { id: $id }) SET n += $properties RETURN n"
    );
    expect(cypher).toContain("$id");
    expect(cypher).toContain("$properties");
  });
});

describe("upsertRelation Cypher template", () => {
  it("builds the expected multi-line merge query", () => {
    const fromLabel = "Person";
    const toLabel = "Repo";
    const relType = "OWNS";
    const cypher = [
      `MERGE (a:${fromLabel} { id: $fromId })`,
      `MERGE (b:${toLabel} { id: $toId })`,
      `MERGE (a)-[r:${relType}]->(b)`,
      `SET r += $properties`,
      `RETURN a, r, b`,
    ].join(" ");
    expect(cypher).toContain("$fromId");
    expect(cypher).toContain("$toId");
    expect(cypher).toContain("$properties");
    expect(cypher).toContain("MERGE (a)-[r:OWNS]->(b)");
  });
});

// ---------------------------------------------------------------------------
// neo4j module — getDriver / closeDriver
// ---------------------------------------------------------------------------
describe("neo4j module", () => {
  beforeEach(() => {
    vi.resetModules();
  });

  it("getDriver returns the same instance on repeated calls", async () => {
    const mockDriver = {
      verifyConnectivity: vi.fn().mockResolvedValue(undefined),
      session: vi.fn(),
      close: vi.fn().mockResolvedValue(undefined),
    };
    vi.doMock("neo4j-driver", () => ({
      default: {
        driver: vi.fn().mockReturnValue(mockDriver),
        auth: { basic: vi.fn().mockReturnValue({}) },
      },
    }));
    const { getDriver } = await import("../neo4j.js");
    const d1 = getDriver();
    const d2 = getDriver();
    expect(d1).toBe(d2);
  });

  it("closeDriver sets driver to null so next getDriver creates a new one", async () => {
    const mockDriver = {
      verifyConnectivity: vi.fn().mockResolvedValue(undefined),
      session: vi.fn(),
      close: vi.fn().mockResolvedValue(undefined),
    };
    const driverFn = vi.fn().mockReturnValue(mockDriver);
    vi.doMock("neo4j-driver", () => ({
      default: { driver: driverFn, auth: { basic: vi.fn().mockReturnValue({}) } },
    }));
    const { getDriver, closeDriver } = await import("../neo4j.js");
    getDriver();
    await closeDriver();
    getDriver();
    // driver factory called twice — once before close, once after
    expect(driverFn).toHaveBeenCalledTimes(2);
  });
});
